package module2.training;


public class VariablesExample {
    public static void main(String[] args) {
        int a = 3, b = 10;
        int result = b / a;
        int module_devision = b % a;
        System.out.println("10 / 3 = " + result);
        System.out.println("10 / 3 = " + module_devision);


    }
}
